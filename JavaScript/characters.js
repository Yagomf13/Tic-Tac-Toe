//--------------------------- Men煤 Desplegable de Emoticonos --------------------------//

function emojiSelect(container) {

    //路路路路路路路路路路路路路路路路路路路路路路路路路路路路 Opci贸n de Personages  路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路//

    const emoticons = [ '', '','X', 'O', '', '', '', '', 'ぐ', '',
                        '', '', '', '', '', 'ぁ', ''];

    //路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路//

    //路路路路路路路路路路路路路路路路路路路路路路路路路 Crear el Elemento <select>  路路路路路路路路路路路路路路路路路路路路路路路路路路路//
    const select = document.createElement('select');
    select.classList.add('containeremoticono'); // A帽adir la clase
    //路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路//

    //路路路路路路路路路路路路路路路路路路路路路路路路 Agregar Opciones al <select>  路路路路路路路路路路路路路路路路路路路路路路路路路路//
    emoticons.forEach(element => {
        const option = document.createElement('option');
        option.value = element;
        option.textContent = element;
        select.appendChild(option);
    });
    //路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路//

    // 
    //路路路路路路路路路路路路路路路路路路路路路路路路 Agregar <select> al Contenedor  路路路路路路路路路路路路路路路路路路路路路路路路//
    container.appendChild(select);
    //路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路路//

}

